
public class IfElse {

	public static void main(String[] args)
	{
		int n=20;
		
		if(n<5)
		{
			System.out.println("The value of n is 5");
		}
		else if(n>30)
		{
			System.out.println("The value of n is 30");
		}
		else if(n>10)
		{
			System.out.println("The value of n is 20");
		}	
		else
		{
			System.out.println("The value of n is not 20");
		}
	}

}
